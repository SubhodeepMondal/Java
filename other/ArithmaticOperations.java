package math;
public class ArithmaticOperations{
    public int addition(int a, int b){
        return a+b;
    }
    public int subtraction(int a, int b){
        return a-b;
    }
    public int multiplication(int a, int b){
        return a*b;
    }
    public int divition(int a, int b){
        return a/b;
    }
}